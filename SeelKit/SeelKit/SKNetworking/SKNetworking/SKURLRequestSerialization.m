    return [NSString stringWithFormat:@"--%@%@", boundary, kSKMultipartFormCRLF];
}

static inline NSString * SKMultipartFormEncapsulationBoundary(NSString *boundary) {
    return [NSString stringWithFormat:@"%@--%@%@", kSKMultipartFormCRLF, boundary, kSKMultipartFormCRLF];
